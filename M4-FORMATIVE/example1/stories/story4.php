    <h2>Story 4: The Old Train Station</h2>
    <p>
        An old train station stood empty for many years. Nobody used it anymore. 
        One day, a group of students decided to restore it. They painted the walls, 
        cleaned the floors, and fixed the broken benches. After three months of hard work, 
        the station looked brand new. The city opened it as a museum. Many people came to learn 
        about the history of trains.
