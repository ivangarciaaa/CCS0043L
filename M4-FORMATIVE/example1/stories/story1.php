    <h2>Story 1: The Lost Key</h2>
    <p>
        Maria found an old key on the street. She didn't know what door it opened. 
        She searched the entire neighborhood for the matching lock. After three hours, 
        she found a small house with a door that had the same keyhole. She turned the key slowly. 
        The door opened to reveal an empty room with a dusty book on a table. The book was a diary 
        from 1952. Maria decided to return it to the library to find its owner.
    </p>
